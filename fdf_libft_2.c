#include "fdf.h"

int	hex_to_dex(char *hex)
{
	int	ret;

	ret = 0;
	while (*hex)
	{
		if (*hex >= '0' && *hex <= '9')
			ret = ret * 16 + (*hex - '0');
		else if (*hex >= 'a' && *hex <= 'f')
			ret = ret * 16 + (*hex - 'a' + 10);
		else if (*hex >= 'A' && *hex <= 'F')
			ret = ret * 16 + (*hex - 'A' + 10);
		hex++;
	}
	return (ret);
}
